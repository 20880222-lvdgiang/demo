let num=100;
console.log("num = ",num);
console.log("num++ =>",num++); //xuất ra num rồi mới ++
console.log("num = ",num);
console.log("----------------------------");
console.log("num = ",num);
console.log("++num =>",++num); //++ trước rồi sau đó mới xuất ra num
console.log("num = ",num);
console.log("----------------------------");
console.log("num = ",num);
console.log("num-- =>",num--); //xuất ra num trước rồi mới --
console.log("num = ",num);
console.log("----------------------------");
console.log("num = ",num);
console.log("--num =>",--num); //-- trước rồi sau đó mới xuất ra num
console.log("num = ",num);
console.log("-------------------------------")
console.log("num = ",num);
console.log("num = num + 1 =>",num+1);
console.log("num = num - 1 =>",num-1);
